package smartbrain.entities;

public class ImageResponse {
	
	Long id;
	Integer entries;
	String name;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getEntries() {
		return entries;
	}
	public void setEntries(Integer integer) {
		this.entries = integer;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
